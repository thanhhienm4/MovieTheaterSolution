using System;
using System.IO;
using System.Security.Cryptography;
using System.Text;
using Microsoft.Extensions.Configuration;

namespace Backend_Done.Authenticate
{
    public class SecurityHelper
    {
        //public static string encryptedText = new ConfigurationBuilder()
